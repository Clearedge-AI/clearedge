class Metadata:
  def __init__(self, heading=None, sub_heading=None, page_no=None, bbox=None, filename=None, doc_type=None, created_at=None):
    self.heading = heading
    self.sub_heading = sub_heading
    self.page_no = page_no
    self.bbox = bbox
    self.filename = filename
    self.doc_type = doc_type
    self.created_at = created_at
